    <h1>How does it work?</h1>
    <p>
        Code (or whatever you want to share) goes in the big editor in the middle. That much should be clear.<br>
        Choose your syntax for highlighting in the <span class="language-hover">language</span> select.<br>
        Enter your name in the <span class="author-hover">author</span> input (or leave it as Anonymous).<br>
        Choose a password in the <span class="password-hover">password</span> input. The encryption key will be derived from it.<br>
        Encryption is optional and <strong>server-side</strong> (for now).
    </p>
